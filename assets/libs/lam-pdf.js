  /**
   * Generate Invoice PDF
   * @param {Object} inv - Invoice data object
   * @param {Object} company - Company profile object
   * @param {Object} customer - Customer object
   */
  function invoice(inv, company, customer) {
    const doc = new LAMPdfDoc();
    const fmt = n => `₹${Number(n||0).toLocaleString('en-IN', {minimumFractionDigits:2})}`;

    doc.docHeader({
      companyName: company?.name || 'Company',
      companyAddr: [company?.address, company?.city, company?.state].filter(Boolean).join(', '),
      companyGST:  company?.gstin,
      companyPhone:company?.phone,
      docTitle:    'TAX INVOICE',
      docNumber:   inv.invoiceNumber || inv.id?.slice(0,10),
      docDate:     inv.invoiceDate ? new Date(inv.invoiceDate).toLocaleDateString('en-IN') : '',
      color:       '#0F172A',
    });

    doc.infoBlock(
      [
        ['Bill To', customer?.name || inv.customerId || '—'],
        ['Address', customer?.address || '—'],
        ['GSTIN',   customer?.gstin   || '—'],
        ['Phone',   customer?.phone   || '—'],
      ],
      [
        ['Invoice No.',   inv.invoiceNumber || '—'],
        ['Invoice Date',  inv.invoiceDate   ? new Date(inv.invoiceDate).toLocaleDateString('en-IN') : '—'],
        ['Due Date',      inv.dueDate       ? new Date(inv.dueDate).toLocaleDateString('en-IN') : '—'],
        ['Payment Terms', inv.paymentTerms  || '—'],
        ['Place of Supply', inv.placeOfSupply || '—'],
        ['Status', inv.paymentStatus?.toUpperCase() || 'UNPAID'],
      ]
    );

    doc.table({
      headers:    ['#', 'Description', 'HSN/SAC', 'Qty', 'Rate', 'Disc%', 'GST%', 'Amount'],
      colWidths:  [24, 180, 64, 40, 70, 40, 40, 76],
      alignments: ['left','left','left','right','right','right','right','right'],
      rows: (inv.items || []).map((item, i) => {
        const base = (Number(item.qty)||0) * (Number(item.unitPrice)||0) * (1-(Number(item.discount)||0)/100);
        return [
          i+1,
          item.description || item.productId || '—',
          item.hsn || '—',
          item.qty || 0,
          fmt(item.unitPrice),
          `${item.discount||0}%`,
          `${item.gstRate||0}%`,
          fmt(base),
        ];
      }),
    });

    const sub  = Number(inv.subtotal)    || 0;
    const disc = Number(inv.discountAmount) || 0;
    const gst  = Number(inv.gstAmount)   || 0;
    const tot  = Number(inv.totalAmount) || (sub + gst - disc);
    const cgst = gst / 2;
    const sgst = gst / 2;

    doc.totals([
      ['Subtotal',       fmt(sub)],
      disc > 0 ? ['Discount',  `-${fmt(disc)}`] : null,
      [`CGST`,           fmt(cgst)],
      [`SGST`,           fmt(sgst)],
    ].filter(Boolean), fmt(tot));

    if (inv.notes) doc.notes(inv.notes, 'Notes / Terms');

    // Amount in words
    doc.space(4);
    doc.text(`Amount in words: ${_amountToWords(tot)}`, MARGIN.left, doc.y, { size: 9, color: BRAND.textLight });
    doc.space(8);

    doc.signatureBlock(['Prepared by', 'Authorised Signatory', 'Customer Seal & Sign']);
    doc.download(`Invoice_${inv.invoiceNumber || inv.id}.pdf`);
  }

  /**
   * Generate Payslip PDF
   */
  function payslip(data) {
    const { emp, company, month, year, gross, deductions, net, breakdown } = data;
    const doc = new LAMPdfDoc();
    const fmt = n => `₹${Number(n||0).toLocaleString('en-IN', {minimumFractionDigits:2})}`;
    const monthLabel = new Date(year, month-1, 1).toLocaleDateString('en-IN', {month:'long', year:'numeric'});

    doc.docHeader({
      companyName: company?.name || 'Company',
      companyAddr: company?.address,
      companyGST:  company?.gstin,
      docTitle:    'PAYSLIP',
      docNumber:   monthLabel,
      docDate:     new Date().toLocaleDateString('en-IN'),
      color:       '#064E3B',
    });

    doc.infoBlock(
      [
        ['Employee Name', emp?.name || '—'],
        ['Designation',   emp?.role || emp?.designation || '—'],
        ['Department',    emp?.department || '—'],
        ['Employee ID',   emp?.employeeId || emp?.id?.slice(0,8) || '—'],
      ],
      [
        ['Month',         monthLabel],
        ['Date of Join',  emp?.joiningDate || '—'],
        ['PAN',           emp?.pan || '—'],
        ['UAN (PF)',      emp?.uan || '—'],
        ['Bank / IFSC',   `${emp?.bank||'—'}`],
        ['Account No.',   emp?.bankAccount || '—'],
      ]
    );

    doc.kpiRow([
      { label: 'Gross Salary',  value: fmt(gross) },
      { label: 'Deductions',    value: fmt(deductions) },
      { label: 'Net Pay',       value: fmt(net) },
      { label: 'Days Paid',     value: data.paidDays || '—' },
    ]);

    doc.space(8);

    doc.table({
      headers:    ['Earnings', 'Amount', 'Deductions', 'Amount'],
      colWidths:  [CONTENT_W * 0.35, CONTENT_W * 0.15, CONTENT_W * 0.35, CONTENT_W * 0.15],
      alignments: ['left','right','left','right'],
      rows: (breakdown || []).map(row => [
        row.earnLabel || '', fmt(row.earnAmt || 0),
        row.dedLabel  || '', fmt(row.dedAmt  || 0),
      ]),
    });

    doc.totals([
      ['Gross Earnings',      fmt(gross)],
      ['Total Deductions',    fmt(deductions)],
    ], fmt(net));

    doc.notes(`This is a computer-generated payslip. Net pay of ${fmt(net)} will be credited to your account.`);
    doc.signatureBlock(['Employee', 'HR Manager', 'Finance']);
    doc.download(`Payslip_${(emp?.name||'').replace(/\s+/g,'_')}_${monthLabel.replace(' ','_')}.pdf`);
  }

