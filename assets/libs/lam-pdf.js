  /**
   * Generate Purchase Order PDF
   */
  function purchaseOrder(po, company, vendor, items) {
    const doc = new LAMPdfDoc();
    const fmt = n => `₹${Number(n||0).toLocaleString('en-IN', {minimumFractionDigits:2})}`;

    doc.docHeader({
      companyName: company?.name || 'Company',
      companyAddr: company?.address,
      companyGST:  company?.gstin,
      docTitle:    'PURCHASE ORDER',
      docNumber:   po.poNumber || po.id?.slice(0,10),
      docDate:     po.date ? new Date(po.date).toLocaleDateString('en-IN') : '',
      color:       '#3B1F6B',
    });

    doc.infoBlock(
      [
        ['Vendor',        vendor?.name    || po.vendorName || '—'],
        ['Address',       vendor?.address || '—'],
        ['GSTIN',         vendor?.gstin   || '—'],
        ['Phone',         vendor?.phone   || '—'],
        ['Email',         vendor?.email   || '—'],
      ],
      [
        ['PO Number',     po.poNumber   || '—'],
        ['PO Date',       po.date ? new Date(po.date).toLocaleDateString('en-IN') : '—'],
        ['Expected By',   po.expectedDate ? new Date(po.expectedDate).toLocaleDateString('en-IN') : '—'],
        ['Delivery To',   po.deliveryLocation || company?.address || '—'],
        ['Terms',         po.paymentTerms     || '—'],
        ['Status',        po.status?.toUpperCase() || '—'],
      ]
    );

    const rows = (items || po.items || []).map((item, i) => {
      const base = (Number(item.qty)||0) * (Number(item.unitPrice)||0);
      const gst  = base * (Number(item.gstRate)||0) / 100;
      return [i+1, item.description||item.productName||'—', item.hsn||'—', item.qty||0, fmt(item.unitPrice), `${item.gstRate||0}%`, fmt(base+gst)];
    });

    doc.table({
      headers:    ['#', 'Item Description', 'HSN', 'Qty', 'Unit Price', 'GST%', 'Total'],
      colWidths:  [24, 200, 64, 50, 80, 50, 80],
      alignments: ['left','left','left','right','right','right','right'],
      rows,
    });

    const sub = (po.items||[]).reduce((s,i)=>(Number(i.qty)||0)*(Number(i.unitPrice)||0)+s, 0);
    const gst = (po.items||[]).reduce((s,i)=>(Number(i.qty)||0)*(Number(i.unitPrice)||0)*(Number(i.gstRate)||0)/100+s, 0);
    doc.totals([['Subtotal', fmt(sub)], ['GST', fmt(gst)]], fmt(sub + gst));

    if (po.notes) doc.notes(po.notes, 'Terms & Conditions');
    doc.signatureBlock(['Prepared by', 'Approved by', 'Vendor Acknowledgement']);
    doc.download(`PO_${po.poNumber || po.id}.pdf`);
  }

  /**
   * Generic report PDF
   * @param {Object} opts - { title, subtitle, kpis, tableHeaders, tableRows, colWidths, notes }
   */
  function report(opts) {
    const doc = new LAMPdfDoc();

    doc.docHeader({
      companyName: opts.company?.name || 'LAM',
      companyAddr: opts.company?.address,
      docTitle:    opts.title?.toUpperCase() || 'REPORT',
      docNumber:   '',
      docDate:     new Date().toLocaleDateString('en-IN'),
    });

    if (opts.subtitle) {
      doc.space(4);
      doc.text(opts.subtitle, MARGIN.left, doc.y, { size: 10, color: BRAND.textLight });
      doc.space(12);
    }

    if (opts.kpis?.length) {
      doc.kpiRow(opts.kpis.slice(0, 4));
    }

    if (opts.tableRows?.length) {
      doc.divider(opts.tableTitle || 'Data');
      doc.table({
        headers:    opts.tableHeaders || [],
        rows:       opts.tableRows,
        colWidths:  opts.colWidths,
        alignments: opts.alignments,
      });
    }

    if (opts.notes) doc.notes(opts.notes);

    const fname = (opts.title || 'Report').replace(/\s+/g,'_');
    doc.download(`${fname}_${Date.now()}.pdf`);
  }

  // ── Number to words (Indian format) ──────────────────────
  function _amountToWords(n) {
    if (!n || isNaN(n)) return 'Zero Rupees Only';
    const ones = ['','One','Two','Three','Four','Five','Six','Seven','Eight','Nine','Ten','Eleven','Twelve','Thirteen','Fourteen','Fifteen','Sixteen','Seventeen','Eighteen','Nineteen'];
    const tens = ['','','Twenty','Thirty','Forty','Fifty','Sixty','Seventy','Eighty','Ninety'];
    const toWords = num => {
      if (num === 0) return '';
      if (num < 20)  return ones[num] + ' ';
      if (num < 100) return tens[Math.floor(num/10)] + (num%10 ? ' ' + ones[num%10] : '') + ' ';
      if (num < 1000) return ones[Math.floor(num/100)] + ' Hundred ' + toWords(num%100);
      if (num < 100000)  return toWords(Math.floor(num/1000)) + 'Thousand ' + toWords(num%1000);
      if (num < 10000000) return toWords(Math.floor(num/100000)) + 'Lakh ' + toWords(num%100000);
      return toWords(Math.floor(num/10000000)) + 'Crore ' + toWords(num%10000000);
    };
    const [intPart, decPart] = Number(n).toFixed(2).split('.');
    let result = toWords(parseInt(intPart)).trim() + ' Rupees';
    if (parseInt(decPart) > 0) result += ' and ' + toWords(parseInt(decPart)).trim() + ' Paise';
    return result + ' Only';
  }

